    private void deleteRows(){

        int numRows = this.model.getRowCount();

        for (int i=numRows-1; i >= 0; i--) {
          this.model.removeRow(i);
          this.jTableTCPPackets.revalidate();
        }

    }

    private void setTableValues(ArrayList<TCPPacket> data){
        
        if(data != null){
            
            TCPPacket tcp;
            String filterValue = "";

            model = (DefaultTableModel) jTableTCPPackets.getModel();

            for(int i=0; i<data.size(); i++){
                
                tcp = data.get(i);
                String temp = new String(tcp.data); 
                
                if(!filterOn){
                    model.addRow(new Object[]{tcp.ack_num, tcp.src_ip, tcp.src_port, tcp.dst_ip, tcp.dst_port, tcp.sequence, temp});
                }else{
                    filterValue = this.jTextFieldfiltertext.getText();
                    
                    if("Src.IP".equals(this.filterBy) && filterValue.equals(tcp.src_ip.toString())){
                        model.addRow(new Object[]{tcp.ack_num, tcp.src_ip, tcp.src_port, tcp.dst_ip, tcp.dst_port, tcp.sequence, temp});
                    }else if("Dst.IP".equals(this.filterBy) && filterValue.equals(tcp.dst_ip.toString())){
                        model.addRow(new Object[]{tcp.ack_num, tcp.src_ip, tcp.src_port, tcp.dst_ip, tcp.dst_port, tcp.sequence, temp});
                    }
                }
                
                
            }
            
            this.meidator.logTCP(data);

        }else{
            JOptionPane.showMessageDialog(null, "There are no Valid Mails available");
        }

    }

