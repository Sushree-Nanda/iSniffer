    @Override
    public void update(Observable o, Object arg) {
        ArrayList<Packet> pkts = (ArrayList<Packet>) arg;
        ArrayList<TCPPacket> tcppkts = new ArrayList<TCPPacket>();
        Packet p;
        TCPPacket tcp;
        
        for(int i=0; i<pkts.size(); i++){
            p = (Packet) pkts.get(i);
            if(p!=null && p instanceof TCPPacket){
                tcp = (TCPPacket) p;
                tcppkts.add(tcp);
            }
        }
        this.setTableValues(tcppkts);
    }

